    void refreshTable() {
        for (int i = 0; i < kontrahenci.size(); i++) {
            
            int finanse = Integer.parseInt(kontrahenci.get(i).getKapital()) + Integer.parseInt(kontrahenci.get(i).getZadluzenie());
            jTable1.setValueAt(kontrahenci.get(i).getNazwa(), i, 0);
            jTable1.setValueAt(kontrahenci.get(i).getAdres(), i, 1);
            jTable1.setValueAt(kontrahenci.get(i).getVat(), i, 2);
            jTable1.setValueAt(kontrahenci.get(i).getKapital(), i, 3);
            jTable1.setValueAt(kontrahenci.get(i).getZadluzenie(), i, 4);
            jTable1.setValueAt(Integer.toString(finanse), i, 5);
            
        }
    }

